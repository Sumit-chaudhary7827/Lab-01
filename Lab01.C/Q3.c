//Write aprogram that prompts the user to enter their name and age. Use char array of size 100 and use %s for input and output.

#include<stdio.h>
int main (){
    char name[100];
    char age[100];

    printf("Sumit");
    scanf("%s", name);

    printf("17");
    scanf("%s", age);

    printf("Sumit %s\n 17 %s\n", name, age);

    return 0;

}
